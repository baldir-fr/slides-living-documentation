/**
 * <p>
 * A code smell is a <strong>surface indication</strong> that usually corresponds to a deeper problem in the system.
 * </p><br/>
 *
 * <p>
 * A smell is by definition something that's <strong>quick to spot</strong> - or "sniffable"
 * </p>
 * <p>
 * <em>A long method is a good example of this - just looking at the code and my nose twitches if
 * I see more than a dozen lines of java. </em>
 * </p><br/>
 *
 * <p>
 * Smells <strong>don't always indicate a problem</strong>
 * </p>
 * <p>
 * <em>Some long methods are just fine. You have to look deeper to see if there is an underlying
 * problem there - smells aren't inherently bad on their own - they are often an indicator of a problem
 * rather than the problem themselves.</em>
 * </p>
 *
 * @see <a href="https://martinfowler.com/bliki/CodeSmell.html">CodeSmell - Martin Fowler</a>
 * @see <a href="https://refactoring.guru/fr/refactoring/smells">Code smells - Refactoring Guru</a>
 */
